from __future__ import annotations

import json
import sqlite3
from datetime import datetime, timezone
from pathlib import Path

import pandas as pd


ROOT = Path(__file__).resolve().parents[1]
REPORT_DIR = ROOT / "report"
ARTIFACT_PATH = REPORT_DIR / "technical_report_artifact.json"
TITLE = "When does DoRA help? A controlled confirmatory study"


def source(
    identifier: str,
    label: str,
    path: str,
    description: str,
    metric_definitions: dict[str, str],
    *,
    sql: str | None = None,
    tables_used: list[str] | None = None,
) -> dict[str, object]:
    query: dict[str, object] = {
        "description": description,
        "engine": "SQLite",
        "language": "sql" if sql else "python",
        "tables_used": tables_used or [path],
        "metric_definitions": metric_definitions,
    }
    if sql:
        query["sql"] = sql
    return {
        "id": identifier,
        "label": label,
        "path": path,
        "query": query,
    }


def main() -> None:
    generated_at = datetime.now(timezone.utc).replace(microsecond=0).isoformat()
    comparisons = pd.read_csv(ROOT / "results" / "extension_paired_comparisons.csv")
    accuracy = pd.read_csv(ROOT / "results" / "extension_accuracy_summary.csv")
    data_regime = pd.read_csv(
        ROOT / "results" / "data_sweep_mlp" / "data_sweep_accuracy_ci.csv"
    )
    data_comparisons = pd.read_csv(
        ROOT / "results" / "data_sweep_mlp" / "data_sweep_paired_comparisons.csv"
    )
    synthetic = pd.read_csv(
        ROOT
        / "results"
        / "synthetic_optimization"
        / "synthetic_optimization_verified_summary.csv"
    )

    primary = comparisons[
        (comparisons["architecture"] == "mlp")
        & (comparisons["scenario"] == "mixed")
        & (comparisons["comparator"] == "lora")
    ].iloc[0]
    replication = comparisons[
        (comparisons["architecture"] == "cnn")
        & (comparisons["scenario"] == "mixed")
        & (comparisons["comparator"] == "lora")
    ].iloc[0]
    lo_ra_plus = comparisons[
        (comparisons["architecture"] == "mlp")
        & (comparisons["scenario"] == "mixed")
        & (comparisons["comparator"] == "lora_plus")
    ].iloc[0]
    matched = comparisons[
        (comparisons["architecture"] == "mlp")
        & (comparisons["scenario"] == "mixed")
        & (comparisons["comparator"] == "lora_matched")
    ].iloc[0]
    gamma08 = synthetic[synthetic["magnitude_strength"] == 0.8].iloc[0]

    data_regime_sql = """SELECT
  adaptation_examples,
  method,
  method_id,
  accuracy_mean_pct,
  accuracy_ci95_low_pct,
  accuracy_ci95_high_pct,
  accuracy_std_pct,
  trainable_parameters,
  n_seeds
FROM data_regime_accuracy
WHERE method_id IN ('dora', 'lora', 'lora_plus')
ORDER BY method_id, adaptation_examples"""
    confirmatory_sql = """SELECT
  architecture,
  scenario,
  mean_delta_pp,
  ci95_low_pp,
  ci95_high_pp,
  paired_effect_dz,
  paired_t_p,
  paired_t_p_holm,
  wins,
  ties,
  losses,
  n_pairs
FROM paired_comparisons
WHERE comparison = 'dora_vs_lora'
ORDER BY architecture, scenario"""
    with sqlite3.connect(":memory:") as connection:
        data_regime.to_sql("data_regime_accuracy", connection, index=False)
        comparisons.to_sql("paired_comparisons", connection, index=False)
        data_chart = pd.read_sql_query(data_regime_sql, connection)
        confirmatory_table = pd.read_sql_query(confirmatory_sql, connection)

    confirmatory_source = source(
        "confirmatory_source",
        "Confirmatory paired comparisons",
        "results/extension_paired_comparisons.csv",
        "Paired DoRA-minus-comparator test metrics generated by analyze_extension.py.",
        {
            "mean_delta_pp": "Mean within-seed test-accuracy difference, DoRA minus comparator, in percentage points.",
            "ci95": "Two-sided 95% Student t interval on paired seed-level differences.",
            "paired_t_p_holm": "Paired t-test p-value adjusted by Holm's method within the declared comparison family.",
        },
        sql=confirmatory_sql,
        tables_used=["paired_comparisons"],
    )
    accuracy_source = source(
            "accuracy_source",
            "Confirmatory accuracy summaries",
            "results/extension_accuracy_summary.csv",
            "Method-level MLP and CNN accuracy summaries generated from raw confirmatory rows.",
            {
                "accuracy_mean_pct": "Arithmetic mean target-test accuracy across confirmatory adaptation seeds, in percent.",
                "trainable_parameters": "Number of model parameters updated during target adaptation.",
            },
        )
    data_regime_source = source(
            "data_regime_source",
            "Nested target-data regime sweep",
            "results/data_sweep_mlp/data_sweep_accuracy_ci.csv",
            "Mixed-shift target-data sweep with frozen MLP configurations and nested balanced samples.",
            {
                "accuracy_mean_pct": "Mean mixed-shift target-test accuracy across 20 paired seeds, in percent.",
                "adaptation_examples": "Total balanced target-training examples across ten digit classes.",
            },
            sql=data_regime_sql,
            tables_used=["data_regime_accuracy"],
        )
    synthetic_source = source(
            "synthetic_source",
            "Synthetic optimization diagnostic",
            "results/synthetic_optimization/synthetic_optimization_verified_summary.csv",
            "Problem-level DoRA optimization and oracle summaries generated by analyze_robustness.py.",
            {
                "relative_weight_error": "Frobenius norm of weight residual divided by target Frobenius norm.",
                "convergence_rate": "Fraction of declared adapter initializations with relative error below 1e-3, averaged across problems.",
            },
        )
    sources = [confirmatory_source, accuracy_source, data_regime_source, synthetic_source]

    manifest = {
        "version": 1,
        "surface": "report",
        "title": TITLE,
        "description": "Technical report for the AIRI Summer School 2026 DoRA project.",
        "generatedAt": generated_at,
        "sources": sources,
        "blocks": [
            {"id": "title", "type": "markdown", "body": f"# {TITLE}"},
            {
                "id": "technical-summary",
                "type": "markdown",
                "body": (
                    "## Technical summary\n\n"
                    "DoRA shows a repeatable but modest advantage on the deliberately difficult mixed domain shift: "
                    f"**{primary.mean_delta_pp:+.2f} pp** versus LoRA in the MLP "
                    f"(95% CI [{primary.ci95_low_pp:+.2f}, {primary.ci95_high_pp:+.2f}]) and "
                    f"**{replication.mean_delta_pp:+.2f} pp** in the CNN "
                    f"([{replication.ci95_low_pp:+.2f}, {replication.ci95_high_pp:+.2f}]). "
                    "The direction replicates across backbones and four target-data budgets, but the strict family-wise "
                    "tests do not establish universal superiority. Pure contrast remains a counterexample in which "
                    "magnitude-only adaptation is more efficient."
                ),
            },
            {
                "id": "key-findings",
                "type": "markdown",
                "body": (
                    "## The mixed-shift signal survives stronger baselines, but multiplicity matters\n\n"
                    f"In the protocol-frozen MLP comparison, DoRA exceeds LoRA+ by **{lo_ra_plus.mean_delta_pp:+.2f} pp** "
                    f"and the nearly parameter-matched LoRA allocation by **{matched.mean_delta_pp:+.2f} pp**. "
                    f"Their Holm-adjusted paired-t p-values are {lo_ra_plus.paired_t_p_holm:.3f} and "
                    f"{matched.paired_t_p_holm:.3f}, respectively. The effect is therefore best interpreted as a "
                    "consistent practical signal with incomplete multiplicity-adjusted certainty, not a binary win."
                ),
                "sourceId": "confirmatory_source",
            },
            {
                "id": "confirmatory-table-block",
                "type": "table",
                "tableId": "confirmatory-table",
            },
            {
                "id": "data-regime-finding",
                "type": "markdown",
                "body": (
                    "## The effect is not tied to one adaptation-set size\n\n"
                    "With all learning rates and allocations frozen from the 400-example validation phase, DoRA remains "
                    "above ordinary LoRA at 50, 100, 200, and 400 target examples. The intervals are wide enough that "
                    "the individual LoRA comparisons remain uncertain, while the larger gap to LoRA+ survives Holm "
                    "correction at several budgets. Nested class-balanced subsets isolate the data-budget change from "
                    "sample replacement."
                ),
                "source": data_regime_source,
            },
            {"id": "data-regime-chart-block", "type": "chart", "chartId": "data-regime-chart"},
            {
                "id": "scope-definitions",
                "type": "markdown",
                "body": (
                    "## Scope, data, and metric definitions\n\n"
                    "The empirical benchmark uses the 1,797-image `sklearn Digits` dataset with one fixed stratified "
                    "source/validation/test split. The primary MLP is `64→128→64→10`; the replication CNN uses two "
                    "convolutions and two linear layers. Target shifts are low contrast, 25° rotation, and an 18° "
                    "rotation plus contrast reduction and Gaussian noise. The primary metric is target-test accuracy. "
                    "All inferential comparisons are paired by target subset, corruption realization, optimizer seed, "
                    "and test set. This is a controlled small-model study, not a transformer-scale reproduction."
                ),
            },
            {
                "id": "experimental-design",
                "type": "markdown",
                "body": (
                    "## Validation-first experimental design\n\n"
                    "Pilot seeds 11/22/33/44/55 select learning rates and, for the MLP, one of three nearly "
                    "parameter-matched rank allocations. Only after every method/shift selection is frozen are "
                    "confirmatory seeds 101–120 (MLP) and 201–210 (CNN) evaluated on target test. Baselines include "
                    "frozen, magnitude-only, LoRA, LoRA+, DoRA, full fine-tuning, LoRA matched to the DoRA budget, and "
                    "DoRA under the LoRA budget ceiling. Primary comparisons report paired mean difference, 95% t "
                    "interval, paired effect size, paired t and Wilcoxon tests, and Holm correction."
                ),
            },
            {
                "id": "synthetic-finding",
                "type": "markdown",
                "body": (
                    "## Capacity is clear; optimization becomes imperfect at extreme magnitude shift\n\n"
                    f"At magnitude strength 0.8, the exact rank-4 LoRA SVD oracle retains mean relative error "
                    f"**{gamma08.lora_oracle_error_mean:.3f}**, while a feasible rank-4 DoRA construction remains near "
                    f"numerical zero. Standard no-op-initialized DoRA training reaches the `<1e-3` criterion in "
                    f"**{100 * gamma08.convergence_rate_mean:.0f}%** of declared runs. The failed initializations are "
                    "retained: the geometry is representable, but strong magnitude changes can still create an "
                    "optimization problem."
                ),
                "sourceId": "synthetic_source",
            },
            {
                "id": "limitations",
                "type": "markdown",
                "body": (
                    "## Limitations, uncertainty, and robustness\n\n"
                    "The study uses a small image dataset and one fixed pretrained instance per architecture. Seed-level "
                    "uncertainty covers few-shot sampling and adaptation optimization, not pretraining variability. The "
                    "same target split appeared in the earlier exploratory phase, so this is a protocol-frozen internal "
                    "confirmation rather than a fully untouched external replication. The synthetic generator belongs "
                    "to the DoRA family by construction and must be read only as a mechanism diagnostic. Negative "
                    "results are material: parameter-matched LoRA ties DoRA on MLP rotation, and magnitude-only wins "
                    "the pure-contrast control."
                ),
            },
            {
                "id": "next-steps",
                "type": "markdown",
                "body": (
                    "## Recommended next step\n\n"
                    "The next decision-useful experiment is an external replication with the official PEFT stack on a "
                    "small pretrained transformer, using LoRA, LoRA+, rsLoRA, and DoRA with matched target modules and "
                    "validation-only tuning. It should vary pretrained seeds or checkpoints and predeclare one primary "
                    "task/metric so that architecture-level generalization can be separated from another exploratory "
                    "benchmark sweep."
                ),
            },
            {
                "id": "further-questions",
                "type": "markdown",
                "body": (
                    "## Further questions\n\n"
                    "- Does the mixed-shift effect persist when the base checkpoint changes?\n"
                    "- Is the advantage caused by expressivity, regularization, or optimizer conditioning in real networks?\n"
                    "- Can a validation-selected magnitude-only/LoRA/DoRA routing rule outperform choosing one adapter universally?"
                ),
            },
        ],
        "charts": [
            {
                "id": "data-regime-chart",
                "title": "Mixed-shift accuracy across target-data budgets",
                "description": "Mean test accuracy across 20 paired seeds; configurations stay fixed across budgets.",
                "dataset": "data_regime",
                "type": "line",
                "encodings": {
                    "x": {"field": "adaptation_examples", "type": "quantitative", "title": "Target examples"},
                    "y": {"field": "accuracy_mean_pct", "type": "quantitative", "title": "Test accuracy, %"},
                    "color": {"field": "method", "type": "nominal"},
                },
                "options": {"points": "always"},
                "source": data_regime_source,
            }
        ],
        "tables": [
            {
                "id": "confirmatory-table",
                "title": "DoRA versus LoRA confirmatory comparisons",
                "description": "Paired test-accuracy differences by architecture and shift.",
                "dataset": "confirmatory_comparisons",
                "columns": [
                    {"field": "architecture", "label": "Backbone", "type": "string"},
                    {"field": "scenario", "label": "Shift", "type": "string"},
                    {"field": "mean_delta_pp", "label": "Mean Δ, pp", "type": "number"},
                    {"field": "ci95_low_pp", "label": "CI low", "type": "number"},
                    {"field": "ci95_high_pp", "label": "CI high", "type": "number"},
                    {"field": "paired_t_p_holm", "label": "Holm p", "type": "number"},
                    {"field": "n_pairs", "label": "Pairs", "type": "number"},
                ],
                "defaultSort": {"field": "mean_delta_pp", "direction": "desc"},
                "source": confirmatory_source,
            }
        ],
    }

    artifact = {
        "surface": "report",
        "manifest": manifest,
        "snapshot": {
            "version": 1,
            "status": "ready",
            "generatedAt": generated_at,
            "datasets": {
                "data_regime": data_chart.to_dict(orient="records"),
                "confirmatory_comparisons": confirmatory_table.to_dict(orient="records"),
                "data_regime_comparisons": data_comparisons.to_dict(orient="records"),
                "accuracy_summary": accuracy.to_dict(orient="records"),
                "synthetic_summary": synthetic.to_dict(orient="records"),
            },
        },
        "sources": sources,
    }
    REPORT_DIR.mkdir(parents=True, exist_ok=True)
    ARTIFACT_PATH.write_text(json.dumps(artifact, indent=2), encoding="utf-8")
    print(ARTIFACT_PATH)


if __name__ == "__main__":
    main()
